// DLA MIESZKAŃCÓW
function dlaMieszkancowOn() {
	var id = "dlaMieszkancow";
	document.getElementById(id + "Img").style.filter = "brightness(10%)";
	document.getElementById(id).style.textAlign = "left";
	document.getElementById(id).style.left = "40%";		
	document.getElementById(id).innerHTML = "Informacje dla mieszkańców - wszystko co możesz załatwić w urzędzie, informacje o odpadach, cmentarzach, bibliotekach";
}
function dlaMieszkancowOut() {
	var id = "dlaMieszkancow";
	document.getElementById(id + "Img").style.filter = "brightness(50%)";
	document.getElementById(id).style.textAlign = "center";
	document.getElementById(id).style.left = "50%";	
	document.getElementById(id).innerHTML = "DLA MIESZKAŃCÓW";
}

